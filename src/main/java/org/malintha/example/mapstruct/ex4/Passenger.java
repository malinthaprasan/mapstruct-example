package org.malintha.example.mapstruct.ex4;

public class Passenger {
    private int age;
    private String name;

    public Passenger(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
